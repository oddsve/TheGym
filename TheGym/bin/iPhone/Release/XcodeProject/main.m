#if defined (DEBUG)
int monotouch_debug_connect (int sock, struct sockaddr_in addr) {
	long flags = 0;
	int res = 0;
	fd_set fds; 
	struct timeval tv; 
	socklen_t len; 
	int val = 0;

	flags = fcntl (sock, F_GETFL, NULL);
	flags |= O_NONBLOCK;
	fcntl (sock, F_SETFL, flags);

	res = connect (sock, (struct sockaddr *) &addr, sizeof (addr));

	if (res < 0) {
		if (errno == EINPROGRESS) {
        		while (1) {
				tv.tv_sec = 2;
				tv.tv_usec = 0;
				FD_ZERO (&fds);
				FD_SET (sock, &fds);
			
				res = select (sock + 1, 0, &fds, 0, &tv);
			
				if (res <= 0 && errno != EINTR) return -5;

				len = sizeof (int);

				if (getsockopt (sock, SOL_SOCKET, SO_ERROR, &val, &len) < 0) return -3;

				if (val) return -4;

				break; 
			} 
		} else {
			return -2;
		}
	}

	flags = fcntl (sock, F_GETFL, NULL); 
	flags &= (~O_NONBLOCK); 
	fcntl (sock, F_SETFL, flags); 

	return 1;
}
